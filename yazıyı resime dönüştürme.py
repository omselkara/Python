def yaz(yer):
    liste = []
    dosya = open(yer,"r",encoding="utf-8")
    dosya = dosya.readlines()
    uzunluk = len(max(dosya,key=len))+1
    for i in dosya:
        liste.append([])
        sayı = 0
        while 1:
            if sayı == len(i)-1:
                break
            else:
                liste[-1].append(sözlük[sözlük2.index(i[sayı])])
                sayı += 1
        for i in range(0,uzunluk-len(i)):
            liste[-1].append((198,198,198))
    liste = np.array(liste,np.uint8)
    img = Image.fromarray(liste)
    for i in range(1,len(yer)+1):
        if yer[-i] == ".":
            isim = yer[:-i+1]
            break
    img.save(isim+"png")
